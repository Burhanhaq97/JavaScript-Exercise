/*
Pair Management
Create a function that takes two numbers as arguments and returns an array which contains these two arguments.

Examples:
makePair(1, 2) ➞ [1, 2]
makePair(51, 21) ➞ [51, 21]
makePair(512124, 215) ➞ [512124, 215]
 */

// solution
const makePair = (val1, val2) => {
  return [val1, val2];
};

console.log(makePair(1, 2));
console.log(makePair(51, 21));
console.log(makePair(512124, 215));